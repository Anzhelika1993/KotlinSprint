package org.example.Variables

fun main() {

    val yearOfSpaceflight: String = "1961"
    var hour = "09"
    var minute = "07"

    println("Год полета: $yearOfSpaceflight")
    println("Час взлета: $hour")
    println("Минута взлета: $minute")

    hour = "10"
    minute = "55"

    println("Время посадки: $hour:$minute")
}